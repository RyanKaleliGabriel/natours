// QUERY MIDDLEWARE (RUNS BEFORE ANY FIND QUERY IS EXECUTED)
// tourSchema.pre('find', function (next) {
// this regex will execute for all queries starting with find e.g findOne
tourSchema.pre(/^find/, function (next) {
  this.find({ secretTour: { $ne: true } });
  this.start = Date.now();
  next();
});

